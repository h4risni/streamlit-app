df = pd.read_excel('Cardheko_RealtimeData.xlsx')
print(df.head())
print(df.shape)
df['selling_price'].describe()
sns.distplot(df['selling_price'])

# Create dummies
df['seller_type_Dealer'] = np.where(df['seller_type'] == 'Dealer', 1, 0)
df['transmission_Auto'] = np.where(df['transmission_type'] == 'Automatic', 1, 0)
df['fuel_Diesel'] = np.where(df['fuel_type'] == 'Diesel', 1, 0)
df['fuel_CNG'] = np.where(df['fuel_type'] == 'CNG', 1, 0)
df['fuel_LPG'] = np.where(df['fuel_type'] == 'LPG', 1, 0)

print(df[['seller_type', 'seller_type_Dealer', 'transmission_type', 'transmission_Auto']].head())

"""Model 1 = Simple Linear regression|
Y = Selling Price |
X = Vehicle Age (Year)
"""

model1 = ols('selling_price ~ vehicle_age', data=df).fit()
print(model1.summary())

y_pred1 = model1.predict(df[['vehicle_age']])
rmse1 = np.sqrt(mean_squared_error(df['selling_price'], y_pred1))
print("RMSE for Model 1:", rmse1)

"""Model 2 = Multiple regression|
Y = Selling Price |
X = Vehicle Age (Year) , Mileage
"""

model2 = ols('selling_price ~ vehicle_age + mileage', data=df).fit()
print(model2.summary())

y_pred2 = model2.predict(df[['vehicle_age', 'mileage']])
rmse2 = np.sqrt(mean_squared_error(df['selling_price'], y_pred2))
print("RMSE for Model 2:", rmse2)

"""Model 3 = Multiple regression| Y = Selling Price | X = Vehicle Age (Year), Mileage, seller_d"""

model3 = ols('selling_price ~ vehicle_age + mileage + seller_type_Dealer', data=df).fit()
print(model3.summary())

y_pred3 = model3.predict(df[['vehicle_age', 'mileage', 'seller_type_Dealer']])
rmse3 = np.sqrt(mean_squared_error(df['selling_price'], y_pred3))
print("RMSE for Model 3:", rmse3)

"""Model 4 = Multiple regression| Y = Selling Price | X = Vehicle Age (Year), Mileage, seller_d, Transmisssion_auto"""

model4 = ols('selling_price ~ vehicle_age + mileage + seller_type_Dealer + transmission_Auto', data=df).fit()
print(model4.summary())

y_pred4 = model4.predict(df[['vehicle_age', 'mileage', 'seller_type_Dealer', 'transmission_Auto']])
rmse4 = np.sqrt(mean_squared_error(df['selling_price'], y_pred4))
print("RMSE for Model 4:", rmse4)

"""Model 5 = Multiple regression| Y = Selling Price | X = Vehicle Age (Year), Mileage, seller_d, Transmisssion_auto, Fuel dummies"""

model5 = ols('selling_price ~ vehicle_age + mileage + seller_type_Dealer + transmission_Auto + fuel_Diesel + fuel_CNG + fuel_LPG', data=df).fit()
