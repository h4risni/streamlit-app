"""Model 7 = Improved version with outlier treatment"""

df7 = df.copy()

Q1 = df7['selling_price'].quantile(0.25)
Q3 = df7['selling_price'].quantile(0.75)
IQR = Q3 - Q1
lower = Q1 - 1.5 * IQR
upper = Q3 + 1.5 * IQR

df7['selling_price'] = np.where(df7['selling_price'] > upper, upper,
                        np.where(df7['selling_price'] < lower, lower, df7['selling_price']))

model7 = ols('selling_price ~ vehicle_age + mileage + seller_type_Dealer + transmission_Auto + fuel_Diesel + fuel_CNG + fuel_LPG + engine + max_power', data=df7).fit()
print(model7.summary())

y_pred7 = model7.predict(df7[['vehicle_age', 'mileage', 'seller_type_Dealer', 'transmission_Auto', 'fuel_Diesel', 'fuel_CNG', 'fuel_LPG', 'engine', 'max_power']])
rmse7 = np.sqrt(mean_squared_error(df7['selling_price'], y_pred7))
print("RMSE for Model 7 (after outlier treatment):", rmse7)

